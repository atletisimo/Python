def dali_cik_cak(num):
    prev = num % 10
    num //= 10

    while num > 0:
        curr = num % 10
        # –∞–∫–æ –∏ –¥–≤–µ—Ç–µ —Å–µ <5 –∏–ª–∏ –∏ –¥–≤–µ—Ç–µ —Å–µ >=5 ‚Üí –Ω–µ –µ —Ü–∏–∫-—Ü–∞–∫
        if (curr < 5 and prev < 5) or (curr >= 5 and prev >= 5):
            return False
        prev = curr
        num //= 10

    return True


